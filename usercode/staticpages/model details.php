			Part 4 shows all intermediate products (bran, grain, straw, draught days, manure and seed) which are produced by the nine farm activities. These are generated in the activity columns, accumulated to the left in the 'intermediate outputs' section (Part 4), then cycled back to the resources pool of Part 3 and from there to the activities which use each such resource/input. As shown by the left-side arrows from Part 4 to Part 3 and thence to Part 2 of Figure 2.5, resources used up by the activities (Part 2) in any year are replenished by the activities producing intermediate products, but these need not exactly balance because some resources may be held over in storage: e.g., in this year 21.5 tonnes of manure are used on the crops but only 15 tonnes were produced; 6.5 tonnes came from storage not recorded in Part 3 of Figure 2.5.
