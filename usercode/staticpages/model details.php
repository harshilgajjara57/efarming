		<p>
			The specialization of Type 3 farming systems is based on a wide range of factors as follows:
		</p>
		<p>
			<blockquote>- Subtype A farms: on commercial/profit opportunity and proximity to urban markets (poultry, pig, dairy and vegetable farms). The development of such profit-oriented peri-urban farm activities is a reflection of economic growth with its demands for intensively produced products with a high income elasticity of demand.
		</p>
		<p>
		   Subtype B farms: on the presence of specific physical/geographical factors (water and good soils for continuous paddy farms; favourable temperature and rainfall regimes for coffee or cacao farms).
		</p>
		<p>
		   Subtype C farms: on necessity (cassava on the poor soils of South Java; yaks and sheep in the high valleys of Nepal and Bhutan).</blockquote>
		</p>
		<p>
			Yet other bases for specialization are historical accident (e.g., smallholder tea in Sri Lanka on lands acquired from previous tea estates); or prior presence of some natural resource (e.g., the sago and nipah farms in the coastal swamps of the Philippines and New Guinea).
		</p>
		<p>
			<B><I>Type 4: Small dependent specialized family farms</I></B>
		</p>
		<p>
			Structurally, except for their lack of independence, Type 4 farms are quite akin to Type 3 farms and contain the same three (A) commercial (B) part-commercial and (C) near-subsistence subtypes; however, they are sufficiently important to be examined as a separate type. The characteristics which set them apart from farms of Types 1, 2 and 3 are their high degree of activity specialization and the lack of real decision-making power possessed by the farm family. The specialization characteristic may be based on the same factors noted above for Type 3 (independent specialized) farms. The dependence characteristic arises from the fact that on Type 4 farms the family is not free to decide what to produce, nor frequently the conditions under which some obligatory activity is to be carried on. This lack of independence can be due to several factors, viz.:
		</p>
		<p>
			<blockquote><I>(a) Terms of tenancy:</i> Tenant farmers are often obliged to produce one or more specific crop or livestock products, as dictated in a landlord-tenant agreement. The tenant-operated vegetable farms of Qatar are an example.
		</p>
		<p>
			<I>(b) Structural integration:</I> In this situation small family farms are integrated more or less closely as the production arm of some larger farming cum processing system. Small tenant-operated farms supplying sugarcane to a mill or leaf to a tobacco-processing factory are common examples of such vertically integrated farms. Not only is the crop which is to be grown specified, but the conditions of production - timing of planting and harvesting, amounts of fertilizers to be used, spraying programs etc. - are also dictated by the controlling authority.
		</p>
		<p>
			<I>(c) Debt:</I> Some agro-industrial units (such as milk processing plants) often provide farmers with input factors (such as cattle, feed and technical assistance) in order to achieve a regular or higher quality supply of their needed raw material (e.g., milk). These advances are usually made in the form of a loan at attractive terms, but often the only way farmers can liquidate this loan (and perhaps eventually regain their independence) is to continue to produce the particular commodity - usually under conditions set by and to the relative advantage of the lender.
		</p>
		<p>
			A second kind of debt, that entered into for consumption rather than production purposes, can also provide the basis for farmer dependence. Thus for generations the small cardamom farmers of the southern Bhutan hills have been indebted to the cardamom traders/money lenders of the towns along the West Bengal border. The only hope these farmers have of liquidating such debt - usually used for food, clothing and household items - is to continue to grow this specialist crop and sell it at whatever terms may be offered by the traders.
		</p>
		<p>
			<I>(d) Government policy directives:</I> In some countries, farmers' lack of independence in production decision making is the result of government power to issue production directives. In Indonesia, e.g., the Government has the broad power to direct that some percentage of those village lands which lie within the command area of each sugar factory be planted to sugarcane. Typically each hectare of land, owned by individuals of the village, might be under sugar for one year during which time it is farmed by the company as part of a larger estate. It then reverts to its owner for three years during which period he or she will operate it as a complete and independent farm, until it is again taken for sugar. During this three-year period the farmer has all normal decision-making powers (crop selection and how each is grown). During the one-year cane phase, for which the farmer receives payment as a 'landlord', he or she has no decision-making power whatsoever. This system thus involves the alternating of two distinctly different farming systems (as shown diagrammatically in Figure 3.1).
		</p>
		<p>
			<I>(e) Lack of alternativ</I>e<I> market outlets:</I> Absence of any real independence in management can also be due to lack of alternative market outlets, especially when the product is too bulky or fragile to be transported far from the farm. For example, most of the small cassava farms of Perak in West Malaysia are located on poor soils which would grow little else except cassava (many are located on tailings or spoil from tin mines). This accounts for their specialization. The second factor, their lack of management independence, is due to the high bulk/low value of their product which must be disposed of to chipping factories in the immediate vicinity. There is little practical possibility of seeking higher prices by transporting the raw cassava further afield. Similar situations face the citronella grass and cinnamon leaf farmers of the Galle-Matara district in southern Sri Lanka. Here again the high bulk/low value of these farm outputs deny the growers any real choice in disposing of their crops to other than the local oil mills under price/quality conditions set by the mills.</blockquote>
		</p>
		<p>
			<I>Source of farm resources:</I> Farms of Type 4 (and Type 3) are usually not self-sufficient in resource-generation; e.g., the continuous cropping of specialized paddy farms (Subtype B in Figure 2.2) is usually possible only because it is based on some purchased package of 'high technology' inputs - HYV seed, artificial fertilizer and agricides. Dependence on commercial inputs is even greater on farms of Subtype A, e.g., purchased feed and veterinary supplies for specialist poultry and pig farms. However, Subtype C farms might exist with only minimal purchased inputs; e.g., the upland near-subsistence maize farms of Nepal, Bhutan and Sarawak are based on use of retained 'local' seed, no artificial fertilizer and no agricides.
		</p>
		<p>
			<I>System boundaries:</I> The boundaries of the specialized farms also vary with subtype. Those of the commercially-oriented farms of Subtype A will interface more to the outside world (i.e., to suppliers of inputs and product markets) than to other farms; however, the boundaries of farm-system Subtypes B and C will tend to be stronger around groups of contiguous farms or all the farms of a hamlet or village in much the same way as the communal boundaries of Type 1 and 2 farming systems set these apart from the outside world.
		</p>
		<p>
			<B><I>Type 5: Large commercial family farms</I></B>
		</p>
		<P>
			Type 5 farms are similar in most respects to estates except that usually the primary beneficiaries are members of an (often extended) family rather than absentee owners or shareholdersThey fall into two subtypes. The first consists of mono-crop farms which are at the fringe of the estate sector proper and which are usually dependent on this estate sector for research, availability of new crop varieties and often for processing and marketing facilities. The 10- to 20-hectare coconut farms of Sri Lanka which exist side-by-side with the large (now nationalized) coconut estates are examples.
		</p>
		<P>
			The second subtype consists of either mono-product or mixed farms which are not part of any estate sector but are organized along commercial lines, e.g., using hired labour, being dependent on purchased rather than farm-produced inputs and, except in the case of tree-crop farms, adjusting the activity or activity mix according to commercial opportunity. The larger cinnamon farms of Galle-Matara and the mixed coconut-dairy farms of Sri Lanka are examples of this subtype. So also are the large 30- to 50-hectare mixed grain-livestock farms of Sind and Punjab.
		</p>
		<p>
			The operating objective of Type 5 farms is profit or utility maximization through market sales. As a group and in pursuit of that objective, they are the most dynamic of the six farm types discussed here.
		</p>
		<p>
			<B><I>Type 6: Commercial estates</I></B>
		</p>
		<p>
			Commercial estates are generally mono-crop in nature. They are largely a colonial legacy, first established to provide cheap raw materials (and later some food and beverage products) to the industries of Europe and North America. This role continues except that they now also serve national industrialization. The chief characteristics of this farm type are as follows:
		</p>
		<p>
			<I>Crops:</I> The main crops on which Type 6 farms were initially based are rubber, sugar, cinchona, cacao, tea, coffee, cinnamon, cloves, nutmeg, coconut and the coarse fibres. Some of the old traditional crops have become uneconomic (sisal and to some extent cinchona); some have become primarily smallholder crops (the spices and coffee); and new estate crops (such as flowers, oil palm and citronella) or improved varieties of old crops have emerged. Recent years have also seen the emergence (usually close to metropolitan areas) of livestock-based estates, particularly for pork and broiler production.
		</p>
		<p>
			<I>On-estate processing:</I> Primary processing is an integral part of the operation of most estates (e.g., tea manufacture, sheet and crepe rubber production, copra curing). This requires a high level of capital investment which, to be fully utilized, requires a flow-type of operation rather than a batch-type. This has two effects. On the one hand it tends to restrict estate production to those crops which yield a fairly uniform year-round flow of produce (tea, rubber, coconut, cocoa etc.). On the other hand it gives estates certain advantages, e.g., quality control, relative to smallholders producing these same products. Some crops which naturally give an intermittent or irregular product flow are also made amenable to continuous estate-type production by relay-planting or chemical control of growth time-patterns (e.g., sugar, sisal and pineapple).
		</p>
		<p>
			<I>Size:</I> Estate size is commonly from 200 to 2 000 hectares but area itself is not an important criterion: a 40-hectare orchid estate will generate about as much income and employment as will a 200-hectare tea estate or a 400-hectare coconut estate.
		</p>
		<p>
			<I>Marketing:</I> Marketing plays a very important role in estate operations. Most estates are jealous of their product reputation or 'mark' and make deliberate attempts at product differentiation. They also maintain close contact with buyers and monitor demand trends. Thus the larger cacao estates of Malaysia might be in daily telex or e-mail contact with buyers in Hamburg and Amsterdam. This contrasts sharply with the situation on smallholder farms growing the same crops: most smallholders have little interest in their product once it leaves the farm gate and, not infrequently, have no knowledge of its use after export.
		</p>
		<p>
			<I>System beneficiaries and operating objectives:</I> Previously, the primary beneficiaries of estates were usually absentee shareholders who employed professional expatriate management and often also a docile expatriate labour force (as was the case in Malaysia, Sri Lanka, Fiji and Mauritius). Thus, before their nationalization, the estates of Sri Lanka were referred to accurately, if somewhat emotively, as 'islands of privilege and prosperity in a sea of poverty'. With exceptions, this situation has changed markedly; consideration of the interests of secondary beneficiaries now receives far greater attention than formerly and these are more widely defined to include the host government, the estate labour force and their dependants, local communities and councils. Profit remains the main operating objective but this is increasingly tempered by the condition that worker retirement schemes, schools, clinics, roads, village welfare centres etc. be provided at estate cost. In short, increasing proportions of operating profit are being diverted from the primary to the secondary beneficiaries of this system type.
		</p>
		<p>
			<I>Management:</I> A mono-product estate system is at once more simple and more complex than the systems found on mixed family farms. Since only one product is usually involved, only one production activity exists, and there is no need to allocate resources among five, six, seven or more competing production enterprises, as on a typical mixed family farm. Also, very little if any of an estate's resources have to be generated within the system's boundaries. (On a typical tea estate usually only fuelwood and hydro power might be produced as inputs to tea production, and even the use of these is declining.) Thus, again in contrast with family farms, there are no resource-generating activities to divert attention from the main production task. However, although only one production enterprise subsystem rather than a multiplicity exists, it is carried on at a sophisticated level. Volume production usually means that per unit profit margins are thin; the wrong decision at some critical time, especially with long-term tree crops, can have serious and long-lasting consequences.
		</p>
		<p>
			Specialization means that the advantages of crop diversification are not available; there is no possibility of making up on the swings what might be lost on the roundabouts. If a Bhutanese farm with one pig suffers an outbreak of swine fever the farmer would probably shrug his shoulders and go off to the paddy field or do something else. If it happened on a 500-sow estate it could spell disaster. Moreover, insofar as most estates are based on one or other of the tree crops, the effects of some sub-optimal decisions (e.g., regarding variety/strain of crop to be planted, spacing, initial fertilizer etc.) might well have long-lasting if not permanent adverse effects, possibly over the 30-year life of a rubber stand, the 65-year life of a coconut stand, or even longer in the case of tea.
		</p>
		<p>
			While not requiring the allocation of resources among enterprises, planning and management of a mono-product estate system requires the explicit recognition, organization and optimization of a large number of agro-technical processes (as discussed in Chapter 5). An example is given in Figure 2.3 which shows the sequential steps in establishing and operating a tea crop. For each step, several alternatives are possible. If this were only one of several crops to be grown (e.g., as on a mixed smallholding), most of the questions listed in Figure 2.3 would not be asked; each operation would proceed on the basis of village tradition, farmer experience or local lore. But on an estate they have to be explicitly asked and answered if the optimal level of long-term sustainable production and profit is to be achieved.
		</p>
		<p>
			In the management planning of estates, three kinds of management analysis can be particularly important: (i) evaluation of some single production enterprise (crop or livestock) over a long time period (as presented in Chapter 10); (ii) the optimization of processes because even small marginal reductions in inputs/costs will become important when spread over many hundreds of hectares or thousands of tonnes of produce (Chapters 5 and 8); and (iii) simulation of estate operations as a whole system under uncertainty (Chapter 11).
		</p>
