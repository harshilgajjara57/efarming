		</p>
		<p>
			<B><I>Model of a Type 1 farm</I></B>
		</p>
		<p>
			Most farms of Type 1 (i.e., small subsistence-oriented family farms) in Asia now have at least some element of commercialisation and generate at least some small amount of cash for the purchase of essential items. At the top end of the structural scale this type merges into the small mixed Type 2 farms; at the bottom end it includes the locally-shifting cultivators of Sarawak-Kalimantan (and these merge into the hunter-gatherers and forest dwellers of New Guinea, Kalimantan, Sarawak and Sumatra).
		</p>
		<p>
			Subsistence or near-subsistence is a condition more often imposed than voluntary: one sub-classification could be on the basis of external causal factors; e.g., the near-subsistence farms of India, the hills of Nepal and parts of Java exist because of shortage of land; those of Bhutan (where land is seldom limiting) because of isolation and the lack of roads and markets; and those of the new settlement areas of Sri Lanka because of the lack of family labour and oxpower to till more than a minimal subsistence area.
		</p>
		<p>
			When examined from the viewpoint of their range of activity, variation of this farm type ranges all the way from being highly mixed to almost mono-crop. In the first of these conditions, this type merges into Type 2 farms. In the second extreme condition these farms are structured around production of a single bulk staple - usually maize, dry paddy, cassava, palm sago or coconut. Secondary foodstuffs and non-food subsistence items are obtained by supplementary activities: fishing; hunting or collecting in nearby forest areas for food or items for sale (birds, monkeys, butterflies, orchids, beeswax, rattan etc.); or by sending a family member off to work somewhere outside the subsistence environment.
		</p>
		<p>
			Although they can generally be described as resource-poor, poverty is not necessarily a characteristic of Type 1 farm families. At one end of the scale the economic condition might be poverty verging on destitution, even starvation. At the other extreme it might be prosperity when that condition is judged by an availability of resources in excess of those needed to maintain a reasonable physical existence.
		</p>
		<p>
			Again the best way of illustrating systems of this subsistence type is with the aid of a model. Such a model is shown in Figure 2.5 which describes the structure and operation of a near-subsistence farm located at about 1 700 m (5 500 feet) above the town of Wangdiprdan in Central Bhutan. The chief features of this farm are its highly diversified activities and its very high level of self-sufficiency. In this example the farm is a prosperous one (as that term was defined above) and its subsistence nature is based on isolation, poor roads, lack of markets and therefore of incentive - indeed opportunity - to enter the commercial world. The model refers to an operating period of one year.<SUP>3</SUP>
		</p>
		<p>
			<blockquote><SUP>3</sup> Data from a 1985 farm survey by Nim Dorji and the senior author. The monetary unit is Ngultrum (Nu) = US 8.7 cents in 1985.</blockquote>
		</p>
		<p>
			The farm's land resources consist of 1.75 langdo<SUP>4</SUP> (0.25 hectares) of dry paddy fields, 1.25 langdo (0.18 hectares) of dry fields, and 4.00 langdo (0.4 hectares) of wetland rented from a local monastery. However, the effective size of this farm is only 5.75 langdo: the 1.25 langdo of dryland is too far away to be easily worked and, since more accessible land can be rented from the local monastery, the dryland portion is not used. (If it were cultivated there would be no market for the extra produce, which would also be surplus to family requirements.)
		</p>
		<p>
			<blockquote><SUP>4</sup> A langdo is the area a pair of oxen can plough in a day: a dry langdo is about one seventh of a hectare and a wet paddy-field langdo is about one tenth of a hectare.</blockquote>
