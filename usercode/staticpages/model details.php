        <p>
            cassava or rice; groundnuts; coconut or sesame.</TD></TR></TABLE>
        </p>
        <p>
        	In general, land is cropped to its maximum intensity, but the number of crop species grown in each of the energy/protein/oil crop subgroups, as well as the area of each, is limited by length of growing season (dependent upon temperature), rainfall occurrence and/or irrigation water supply.
        </p>
        <p>
        	Livestock, whether fish, poultry or larger animals, are typically important on Type 2 farms. They are closely integrated with the crop activities, and here - unlike the situation on farms in developed countries - they are kept for a range of purposes: direct production, draught power (except on the smallest farms), transport, manure production to sustain field and pond fertility levels, and as a store of wealth. The combination of livestock with crops results in a large number of activities, and an even larger number of different farm products.
		</p>
		<p>
			A special subtype of this highly-mixed farm type consists of the forest-garden farms of the wet tropics as found in Kerala, Sri Lanka, Malaysia and Indonesia. These consist of both whole farms, e.g., the forest-garden farms of Kandy in Sri Lanka - see McConnell (1992) - or the house-yard parts of farms forested with a more or less dense mix of economic species as in the Pekarangan lands of Java. Except for poultry, livestock are relatively unimportant on this subtype.
		</p>
		<p>
			<I>Farm system boundaries:</I> Discussion of system boundaries in Chapter 3 mainly relates to farms of this type. Briefly, boundaries of Type 2 farm systems (and of Types 1, 3 and 4) segregate them distinctly from the external world, but the boundaries between individual farms are relatively weak. In contrast with farms in developed countries, they often have much stronger links to and interdependence with other farms in the local community than they do with the outside world, i.e., with commercial input suppliers and markets for their outputs. Through such practices as exchange of labour and animal services among farms, group farming, community work ('gotong royong' in Indonesia) to develop irrigation channels, village roads etc., barter of food grains for animal products, and village-level savings' mobilization ('arisan' in Indonesia), each farm-household unit is a link in an often highly developed agro-socioeconomic network within the local community. Whatever the basis for such informal integration - culture, religion, isolation - its effect is to provide strong structural boundaries around groups of farms, hamlets and villages rather than around individual farms. Each Type 2 farm is very much a part of the community and often could not function effectively if divorced from it. It might well be said that 'No such farm is an Island, entire unto itself.'
		</p>
		<p>
			<I>Activity and product diversity:</I> Diversity, or the degree to which farm income (however measured) is derived from a range of activities and products rather than from a single source, is discussed in Section 6.2.4. Type 2 farms are typically the most diverse of all farms. Diversity has three elements: the number of crop/livestock activities present; the number of products obtained from those activities; and the number of ways in which each product can be used or disposed of.
		</p>
		<p>
			The mixed farms of the Punjab commonly consist of four to six crop activities and three to six livestock activities; those of Bhutan somewhat fewer. In Sri Lanka the forest-garden farms commonly grow up to 16 or so tree and vine species producing 20 to 30 different products; and since each can be disposed of in up to four different ways (sold/bartered, consumed, processed, stored), such a system will possibly generate some 60 or so end products. This contrasts sharply with the situation on farms of Type 6, the estates producing a single product (tea, rubber etc.) with a single end use (sale).
		</p>
		<p>
			Even a common field crop such as maize may be managed so as to yield four or five primary products (green pick, dry grain, fodder leaves and stalks, fuel, live stripped stalks as supports for a companion bean crop), and two or three subsequent processed products (maize cakes - an important kitchen industry in parts of Bhutan, alcohol etc.).
		</p>
		<p>
			Diversification of Type 1 and 2 farms has several bases. Broadly it follows from their sustenance orientation. In remote tracts of Nepal and Bhutan it is a necessity. In the Punjab it results largely from the possibility of growing a wide range of summer and winter crops and combining these with livestock. On the closely integrated vegetable-poultry-pig-fish farms of West Malaysia and Sarawak it results from a business-like approach to profit maximization. On the forest-garden farms of Kandy in Sri Lanka it results partly from historical circumstance (the wide botanical base provided by Indian, Arab, Portuguese, Dutch and British immigrants and colonizers) and partly from a tendency of the Kandyans to plant a tree/vine/shrub in any vacant space. (If they did not, the space would soon be filled anyway through natural seed fall and germination.)
		</p>
		<p>
			<I>Sources and uses of farm resources:</I> An important characteristic of Type 2 farms (and of farms of Type 1) is the high proportion of farm and household resources generated on the farm and, correspondingly, the low level of dependence on purchased inputs. Further, where purchased resources are used, it is common practice to restrict their use to cash crops (cotton, sugarcane, tobacco etc.) with non-cash crops receiving no purchased inputs or being grown on the residual fertilizer (and often soil tilth and soil moisture) from some previous commercial crop. Those farms which do use purchased inputs often operate at dual levels of technology - 'advanced' for some main crop, 'traditional' for the rest. Farm-generated resources including food supply (as distinct from purchased resources) are obtained in a wide variety of ways as follows:
		</p>
		<p>
			<blockquote><font FACE="Symbol">&#183;</font> By operating separate specific-purpose resource-generating activities. These, as distinct from cash-generating activities, are relatively important on farms of Types 1, 2 and 3. The most common example is livestock kept primarily for manure production (as well as for other purposes). Growing a green manure crop serves a similar purpose. Growing and lopping the leaves from leguminous trees for paddy fertilizer is still common in Java. Such activities need not be elaborate: in Bali and Sri Lanka the most common 'resource-generating' activity is simply growing a clump of bamboo in the house-yard (for construction material, produce containers, fences, water pipes).
		</p>
		<p>
			<font FACE="Symbol">&#183;</font> Through simple apportionment of part of a commercial crop for household use as food or livestock feed.
		</p>
		<p>
			<font FACE="Symbol">&#183;</font> By carrying on parallel crop activities by growing one variety/type for the market and another for the family's own use. The first, typically a high-yielding improved variety, might be deficient in taste and storability but will generate cash. The second might be capable of long storage and possess other qualities valuable in rural but not in sophisticated urban markets.
		</p>
		<p>
			<font FACE="Symbol">&#183;</font> Through extensive use of all by-products: stalks of maize, tobacco, pigeon pea, cotton etc. as household fuel; wheat straw for mud brick making; etc. With some crops - especially tree crops - the 'by-products' may take on such relative importance in the range of their uses as to make unclear just what the 'main' crop is.
		</p>
		<p>
			<font FACE="Symbol">&#183;</font> By growing/keeping generally low-yielding but multi-purpose types of crops and livestock rather than high-yielding specific-purpose types. Maize, wheat etc. might be grown nearly as much for green/dry animal fodder as for grain, or - in the case of maize - as a standing trellis for some following climbing crop, e.g., beans or cucumbers. (Thus the not uncommon observation that farmers are 'backward' because they do not adopt 'improved' varieties is often based on ignorance as to the real reasons why the 'old' varieties are grown.)
		</p>
		<p>
			<font FACE="Symbol">&#183;</font> Finally, resources are also generated by the multiple use of farm capital. A common example is provided by farm boundary and roadside fences. In the Matale district of Sri Lanka most of the fences consist of kapok trees planted at very close spacing. They also support pepper vines and thus yield four 'products': kapok floss and seed (for oil) and black pepper, as well as field security. Farm fences in the Yogyakarta-Boyolali area of Central Java are used to generate a wider range of resources (or to directly produce a marketable commodity). There are four main types: (a) bamboo-lattice fences, invariably used also as supports for long-beans; (b) napier grass fences consisting of a single row of grass, laced together into an upright position by a strip of bamboo about a metre above ground level which permits the growing top of the fence to be regularly cut for cattle feed; (c) live leguminous trees whose leaves are harvested periodically for cattle feed or seasonally as field green manure; and (d) cassava fences, formed by planting cassava very close (15 to 20 cm) and weaving one or two bamboo strips through the line of cassava stems at about one metre from ground level. The cassava stems are then used also as a bean trellis.</blockquote>
		</p>
		<p>
			Following is a partial list of the farm and household resources/inputs/capital equipment commonly generated on farms of Types 1 and 2. The list suggests the high level of self-sufficiency that characterizes these farm-system types, especially in isolated areas.
		</p>
		<p>
			<blockquote>- <I>Labour and power:</I> all labour (except at peak periods, then by labour exchange or mutual help); all ox/buffalo/camel draught power, grain grinding power and transport; ox/buffalo treading power for grain threshing (mainly of paddy).
		</p>
		<p>
		    <I>Crop inputs:</I> most seed, animal manure (with a chemical fertilizer supplement on some cash crops); lopped high-nitrogen tree leaves for paddy fields (wet tropics); packing materials for market (woven bamboo baskets, teak and plantain leaf wrappers).
        </p>
        <p>
            <I>Capital equipment:</I> all ploughs, harrows, rakes and levellers from farm or village timber; all animal harness and repairs; fences (live kapok, areca palm, napier grass, cassava, woven bamboo); sugarcane roller crushers; oil extractors; irrigation water delivery systems (bamboo pipes in parts of Bali and the Madiun Valley of Indonesia).
        </p>
        <p> 
        	<I>Buildings and household:</I> building panels/roofing/flooring (woven bamboo and sago/nipah/coconut leaves); buckets, containers (areca palm spathe and woven bamboo); cordage, twine, ropes; granary containers (woven, wood, packed earth); stoves and jaggery sugar boilers (packed earth); household fuel (crop residues, tree prunings); domestic light (coconut oil, butter lamps).</blockquote>
		</p>
		<p>
			<B><I>Type 3: Small independent specialized family farms</I></B>
		</p>
		<p>
			The key characteristics of Type 3 farms are (a) their specialization in some particular crop or livestock activity which distinguishes them from the mixed farms of Types 1 and 2; and (b) their management independence which distinguishes them from Type 4 farms.
		</p>
		<p>
			Type 3 farms fall into three subgroups according to their management orientation/purpose and type of income: (A) commercially-oriented farms, and family sustenance-oriented farms which achieve this objective through either (B) sale of part of their production (which makes them of necessity part-commercial farms) or (C) multiple-use of produce from their single specialized activity and/or barter of some of this produce for necessary commodities/goods which cannot be produced or purchased. In this latter situation such farms are also a subtype of subsistence farms (Type 1), but differ from the main body of near-subsistence farms in that only one main production activity is pursued. A sub-classification of Type 3 farms is shown in Figure 2.2. Some examples of these Type 3 farm subtypes are noted below. Probably the most important are the Subtype B near-continuous paddy farms of the wet tropics.
		</p>
			<I>Subtype A (commercial):</I>
		<p>
			<blockquote><font FACE="Symbol">&#183;</font> small farms specializing in poultry, pig, dairy or vegetable production around metropolitan areas.
			</p>
		<p>
			<font FACE="Symbol">&#183;</font> orchid and horticulture farms.
		</p>
		<p>
			<font FACE="Symbol">&#183;</font> vegetable farms in upland areas throughout Malaysia, North Sumatra and Java.
		</p>
		<p>
			<font FACE="Symbol">&#183;</font> smallholder rubber, oil palm or pepper holdings in Malaysia and Indonesia.
		</p>
		<p>
			<font FACE="Symbol">&#183;</font> citronella and cinnamon farms in southern Sri Lanka.</blockquote>
		</p>
		<p>
			<I>Subtype B (part-commercial):</I>
		</p>
		<p>
			<blockquote><I><font FACE="Symbol">&#183;</font> </i> continuous and near-continuous paddy farms of the monsoon lands.<BR><font FACE="Symbol">&#183;</font> upland/dryland maize and cassava farms.<BR><font FACE="Symbol">&#183;</font> smallholder coffee or cacao farms.</blockquote>
		</p>
		<p>
			<I>Subtype C (near-subsistence):</I>
		</p>
		<p>
			<blockquote><font FACE="Symbol">&#183;</font> near-subsistence maize farms of East Bhutan, Nepalese hills and Sarawak.<BR><font FACE="Symbol">&#183;</font> cassava-based farms on poor soil in South Java.<BR><font FACE="Symbol">&#183;</font> sago farms of South East Asia and New Guinea.<BR><font FACE="Symbol">&#183;</font> yak/sheep migratory farms of the high Himalayan valleys.</blockquote>
