         <I>Mode 1</I> સમાવેશ થાય છે<I>નિયમિત કામગીરી અને નિયંત્રણ</I> પ્રવૃત્તિઓ.તે ખરેખર ફાર્મ, એસ્ટેટ, સહકારી અથવા અન્ય ખેત આધારિત ઉત્પાદક / માર્કેટિંગ સંસ્થાના રોજ-બ-રોજના સંચાલન અને સંચાલનથી સંબંધિત છે. આને વ્યવહારુ અથવા 'મૂડિ-બૂટ્સ' ફાર્મ મેનેજમેન્ટ તરીકે માનવામાં આવે છે. આ સ્થિતિમાં મેનેજમેન્ટ મોટે ભાગે વર્તમાન ચર્ચાના અવકાશની બહાર છે, સિવાય કે અહીં ચર્ચા થયેલ સિસ્ટમની વિભાવનાઓ, તે આશા રાખવામાં આવે છે, વ્યવહારિક (એટલે ​​કે, મોડ 1) મેનેજમેન્ટને માર્ગદર્શન આપવા સિદ્ધાંતો પ્રદાન કરે છે.
      </p>
      <p>
