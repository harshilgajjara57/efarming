			The farm family consists of five adults, all able to work full-time if necessary, i.e., a population density and potential workforce of six persons per hectare. It is assumed that this family could provide a potential supply of 1 200 labour-days annually. Livestock resources are also high relative to farm size: two oxen, two milkcows, three young cattle, three pigs, three hens. Cattle are grazed off the farm on common lands for more than half the year.
