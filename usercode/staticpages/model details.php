      </p>
       <p>
       	   છ મૂળ ફાર્મ પ્રકારો છે:
       </p>
		<p>
			<blockquote>Type 1. <I>નાના નિવારણ લક્ષી કુટુંબ ફાર્મ્સ.</I>
	   <p>
	   	  Type 2. <I>નાના સેમિ-સબસિસ્ટન્સ અથવા પાર્ટ-કોમર્શિયલ ફેમિલી ફાર્મ</I>, સામાન્ય રીતે એક અડધીથી બે હેકટરમાં, પરંતુ વિસ્તાર સારો માપદંડ નથી: તે જ મૂળભૂત માળખું પંજાબ, સિંધ અને પાકિસ્તાનના ઉત્તર પશ્ચિમ ફ્રન્ટીયર પ્રાંતોમાં 20-થી-30-હેક્ટર જેટલા મોટા ફાર્મ પર મળી શકે છે.
	  </p>
		<p>Type 3. <I>નાના સ્વતંત્ર વિશિષ્ટ કુટુંબ ખેતરો.</I>
		</p>
