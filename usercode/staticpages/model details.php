			In Part 2 of the structural model, all productive activities (subsystems) are listed in separate activity columns. There are nine of these (plus one external resource-generating activity): tending cattle, pigs and poultry; growing paddy, wheat, buckwheat, mustard, vegetables/chillies and fruit. The external resource-generating activity consists of one family member working part-time off the farm (for local government, clearing paths and roads after landslides).
