        </p>
         <p>Type 5. <I>મોટા વ્યાપારી કુટુંબ ફાર્મ્સ </ i>, સામાન્ય રીતે સંશોધિત એસ્ટેટ લાઇન્સ સાથે વિશિષ્ટ અને સંચાલન કરે છે.
         </p>
        <p>
        	Type 6. <I>કોમર્શિયલ એસ્ટેટ</I>,સામાન્ય રીતે મોનો-પાક અને ભાડે સંચાલન અને ગેરહાજરી માલિકી સાથે.
        </blockquote>
        <p>
        	છ ફાર્મ પ્રકારોમાંથી દરેક હવે બદલામાં ચર્ચામાં છે.
        <p>
        	<B><I>Type 1: Small subsistence-oriented family farms</I></B>
		<p>
			There are two main subtypes. First, and of lesser numerical importance, are those based on only one or two crops or livestock types (e.g., on maize or cassava or coconuts; or on yaks or camels). Some farms of this subtype are based more on exploitation or management of a local natural resource - in the extreme case, by use of shifting cultivation or by nomadism - than on deliberate choice of their main farm enterprise (e.g., on indigenous sago palm, palmyrah, coconut or nipah). However, the main group of Asian subsistence-oriented farms is based on a wide range of crops and animal types. This second subtype is of necessity more highly mixed than are Type 2 part-commercial farms. Farms which are completely self-sufficient are rare, but self-sufficiency remains the operating objective and, if forced by circumstances, farms of this type could exist in isolation from the outside world. The structure of a Type 1 farm is exemplified in Figure 2.5 below. The focus for evaluation and analysis of Type 1 farms is the household rather than the farm component of the system. However, Type 1 farms have most of the characteristics of Type 2 farms and these are discussed below in relation to this latter type.
		</p>
		<p>
			<B><I>Type 2: Small semi-subsistence or part-commercial family farms</I></B>
		</p>
		<p>
			This type is predominant throughout South and South East Asia in terms of the number of such units, the large number of people supported by them and the total volume of their production - especially of basic foodstuffs.
		</p>
		<p>
			<I>Operating objective:</I> The general operating objective of this farm type is family sustenance, pursued first by production of foodstuffs for consumption and of produce/materials for use on the farm, and second by generation of some cash income for the purchase of (a) non-farm produced food essentials (salt, tea etc.); (b) other essentials such as clothing, medicines, transistor radio, batteries etc.; and (c) some farm inputs (such as agricides and fertilizer). Such cash is obtained primarily by sale of commodities which are surplus to family requirements, and secondarily - where this is possible - by production and sale of some cash crop raised specifically for this purpose. The comparative operating objectives of this and other farm types are discussed in Chapter 6.
		</p>
		<p>
			<I>Production activities:</I> Type 2 farms can be further classified according to geographical occurrence (e.g., wet tropics; sub-tropics of India/Pakistan; temperate zone of North India, Nepal, Bhutan), and by whether these farms are dryland (as most are) or part irrigated. However, they are all basically similar in their crop activities which consist essentially of one or more staple food crops plus a leguminous protein source plus an oil crop (see Section 9.6.3). Some examples of geographically typical crop mixes are:
